
import React, { useState } from 'react';
import { db } from '../../services/database';
import { UserProfile, UserRole } from '../../types';

interface AuthPageProps {
  onAuthSuccess: (user: UserProfile, isNew: boolean) => void;
}

const AuthPage: React.FC<AuthPageProps> = ({ onAuthSuccess }) => {
  const [isLoading, setIsLoading] = useState(false);

  const handleGoogleSignIn = async () => {
    setIsLoading(true);
    // Simulated Google Auth Flow
    setTimeout(async () => {
      const email = 'user@gmail.com';
      const existing = await db.getById<any>('users', email);
      
      if (existing) {
        const profile = await db.getById<UserProfile>('profiles', existing.profileId);
        if (profile) {
          await db.save('session', { id: 'current', profileId: profile.id });
          onAuthSuccess(profile, false);
        }
      } else {
        const profileId = Date.now().toString();
        // Fix: Removed 'insurancePolicies' property because it is not defined in the UserProfile interface
        const newProfile: UserProfile = {
          id: profileId,
          name: 'Google User',
          age: 20,
          gender: 'Not specified',
          country: 'India',
          conditions: [],
          role: UserRole.PATIENT,
          isPrimary: true,
          history: []
        };
        
        await db.save('profiles', newProfile);
        await db.save('users', { email, profileId });
        await db.save('session', { id: 'current', profileId });
        onAuthSuccess(newProfile, true);
      }
      setIsLoading(false);
    }, 1500);
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-white p-6">
      <div className="w-full max-w-sm text-center space-y-12 animate-in fade-in zoom-in-95 duration-700">
        <div className="space-y-4">
          <div className="w-20 h-20 bg-blue-600 rounded-[28px] flex items-center justify-center mx-auto shadow-2xl shadow-blue-200">
            <svg className="w-10 h-10 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M13 10V3L4 14h7v7l9-11h-7z" />
            </svg>
          </div>
          <h1 className="text-4xl font-black text-slate-900 tracking-tight">MedOS</h1>
          <p className="text-slate-400 font-bold text-xs uppercase tracking-[0.2em]">Neural Healthcare Engine</p>
        </div>

        <div className="space-y-6">
          <button 
            onClick={handleGoogleSignIn}
            disabled={isLoading}
            className="w-full flex items-center justify-center gap-4 py-5 bg-white border-2 border-slate-100 rounded-3xl font-black text-slate-700 hover:bg-slate-50 transition-all hover:border-blue-200 active:scale-95 group shadow-sm"
          >
            {isLoading ? (
              <div className="w-5 h-5 border-2 border-blue-600 border-t-transparent rounded-full animate-spin"></div>
            ) : (
              <svg className="w-6 h-6" viewBox="0 0 24 24">
                <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z"/>
                <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
              </svg>
            )}
            <span className="text-sm">Continue with Google</span>
          </button>
          
          <p className="text-[10px] text-slate-400 font-bold uppercase tracking-widest px-8 leading-relaxed">
            By signing in, you agree to our clinical data processing terms and neural privacy protocols.
          </p>
        </div>
      </div>
    </div>
  );
};

export default AuthPage;
